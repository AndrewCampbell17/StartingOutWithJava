package chapter2;

public class NameAndInitials {

	public static void main(String[] args) {
		String firstName = "Andrew",
				middleName = "Jordan",
				lastName = "Campbell";
		
		char firstInitial = 'A',
				middleInitial = 'J',
				lastInitial = 'C';
		
		System.out.println(firstName + " " + middleName + " " + lastName);
		System.out.println(firstInitial +""+ middleInitial +""+ lastInitial);
		

	}

}
